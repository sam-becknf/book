  def create
    @post = Post.new(post_params)

    respond_to do |format|
      if @post.save
        format.html { redirect_to @post, notice: "Book was successfully created." }
        format.json { render :show, status: :created, location: @post }
      else
        format.html { render :new, status: :unprocessable_entity }
        format.json { render json: @post.errors, status: :unprocessable_entity }
      end
    end
  end

  def update
    respond_to do |format|
      if @post.update(post_params)
        format.html { redirect_to @post, notice: "Book was successfully updated." }
        format.json { render :show, status: :ok, location: @post }
      else
        format.html { render :edit, status: :unprocessable_entity }
        format.json { render json: @post.errors, status: :unprocessable_entity }
      end
    end
  end


  def versions
    post = Post.find(params[:post_id])
    @versions = post.versions
  end

  def restore_version
    @post = Post.find(params[:post_id])
    @post.update_column(:title, params[:title])
    @post.update_column(:content, params[:content])
    respond_to do |format|
      format.html { redirect_to @post, notice: "Book was successfully updated." }
      format.json { render :show, status: :ok, location: @post }
    end
  end

